package cl.gmo.pos.venta.web.beans;

public class ListaPresupuestoCabeceraBean {
	private String codigoCabecera;
	private String fecha;
	private String agente;
	private String cliente;
	private String nombre;
	private String apellido;
	private String descuento;
	private String cambio;
	private String nif_cliente;
	
	
	public String getNif_cliente() {
		return nif_cliente;
	}
	public void setNif_cliente(String nif_cliente) {
		this.nif_cliente = nif_cliente;
	}
	public String getCodigoCabecera() {
		return codigoCabecera;
	}
	public void setCodigoCabecera(String codigoCabecera) {
		this.codigoCabecera = codigoCabecera;
	}
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	public String getAgente() {
		return agente;
	}
	public void setAgente(String agente) {
		this.agente = agente;
	}
	public String getCliente() {
		return cliente;
	}
	public void setCliente(String cliente) {
		this.cliente = cliente;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getApellido() {
		return apellido;
	}
	public void setApellido(String apellido) {
		this.apellido = apellido;
	}
	public String getDescuento() {
		return descuento;
	}
	public void setDescuento(String descuento) {
		this.descuento = descuento;
	}
	public String getCambio() {
		return cambio;
	}
	public void setCambio(String cambio) {
		this.cambio = cambio;
	}
	
	

}
